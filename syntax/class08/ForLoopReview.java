package com.syntax.class08;

public class ForLoopReview {
//want to creat multiplication table
	//1*1=1-------1*10=10
			
	public static void main(String[] args) {
		
		int num=7;
		int mult;
		for (int i=1; i<=10; i++) {
			mult=num*i;
			System.out.println(num+" * "+i+" = " +mult);
		}
		
		
		
	}
}
