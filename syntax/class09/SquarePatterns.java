package com.syntax.class09;

public class SquarePatterns {
	public static void main(String[] args) {
		//rows goes horizontally , rows will be controlled by outer loop, top to bottom
		// columns goes vertically,columns are controlls by inner loop, next to each other
		for (int r = 1; r<= 6; r++) {
			for(int c=1; c<=8; c++) {
			System.out.print("* ");
			
			}
			System.out.println();
		}

	}
}
